    private void limpar(){
        tfAttEndereco.setText("");
        tfAttTelefone.setText("");
        attCrmvBox.removeAllItems();
        attCrmvBox.addItem("Novo CRMV");
        listarCrmv();
        tfAttSenha.setText("");
    }
    
    private void listarCrmv() {
        VeterinarioDAO vetDao = new VeterinarioDAO();
        ArrayList<Veterinario> listaVets = vetDao.buscaVeterinarios();
        
        String crmvs = null;
        
        for (int i = 0; i < listaVets.size(); i++) {
            crmvs = (String) listaVets.get(i).getCRMV();
            attCrmvBox.addItem(crmvs);
        }
    }

    private void attEndereco(){
        auDao.atualizarEndereco(cpf, tfAttEndereco.getText());
    }
    
    private void attTelefone(){
        auDao.atualizarTelefone(cpf, tfAttTelefone.getText());
    }
    
    private void attCRMV(){
        auDao.atualizarCrmvAux(cpf, (String) attCrmvBox.getSelectedItem());
    }

    private void attSenha(){
        auDao.atualizarSenha(cpf, tfAttSenha.getText());
    }

    private void sair(){
        Menu m = new Menu();
        m.setVisible(true);
        this.dispose();
    }
    
