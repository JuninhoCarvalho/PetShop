    protected void passagem(String cpf){
        Auxiliar au = auDao.buscarAuxiliarPorCpf(cpf);
        
        labNome.setText(au.getNome());
        labCpf.setText(au.getCpf());
        labEmail.setText(au.getEmail());
        labEspec.setText(au.getEspecialidade());
        labEndereco.setText(au.getEndereco());
        labTele.setText(au.getTelefone());
        labSenha.setText("********");
        
        this.cpf = cpf;
        this.senha = au.getSenha();
        this.crmvAtual = au.getCRMVVet();
    }
    
    private void atualizar() {
        if(!(tfSenhaAtual.getText().equals(senha))){
            JOptionPane.showMessageDialog(rootPane, "Senha Atual Incorreta!");
        }else if("".equals(tfAttEndereco.getText()) && ("(  )     -    ".equals(tfAttTelefone.getText())) 
                && ("".equals(tfAttSenha.getText())) && "Novo CRMV".equals(attCrmvBox.getSelectedItem())){
            JOptionPane.showMessageDialog(rootPane, "Insira alguma atualiza√ß√£o!");
        }else{
            if(!("".equals(tfAttEndereco.getText()))){
                attEndereco();
                sai = true;
            }    
            if(!("".equals(tfAttTelefone.getText()))){
                attTelefone();
                sai = true;
            }
            if(!("".equals(tfAttSenha.getText()))){
                attSenha();
                sai = true;
            }
            if(!("Novo CRMV".equals(attCrmvBox.getSelectedItem()))){
                if(crmvAtual.equals(attCrmvBox.getSelectedItem())){
                    JOptionPane.showMessageDialog(rootPane, "Este j√° √© o seu CRMV Atual");
                    sai = false;
                }else{
                    attCRMV();
                    sai = true;
                }
            }    
            if(sai)
                sair();
        }
    }
    
