    private void cadastrar() {
        if("".equals(tfNome.getText()) || "".equals(tfCPF.getText()) || 
                "".equals(tfEspecialidade.getText()) || "".equals(tfTelefone.getText()) || 
                "".equals(tfEndereco.getText()) || "".equals(tfEmail.getText()) || 
                "".equals(tfSenha.getText())){
            
            JOptionPane.showMessageDialog(rootPane, "Preencha os campos corretamente!");
        }
        else{
            nome = tfNome.getText();
            cpf = tfCPF.getText();
            especialidade = tfEspecialidade.getText();
            CrmvVet = (String)crmvBox.getSelectedItem();
            endereco = tfEndereco.getText();
            telefone = tfTelefone.getText();
            email = tfEmail.getText();
            senha = tfSenha.getText();
            CNPJ = PetShop.getCNPJ();
            Auxiliar aux = new Auxiliar(cpf, nome, endereco, telefone, senha, email, especialidade, CNPJ, CrmvVet);
            AuxiliarDAO auxDAO = new AuxiliarDAO();
            auxDAO.inserirAuxiliar(aux);
            PetShop.setQtdFuncionarios(PetShop.getQtdFuncionarios()+1);
            PetShopDAO ptDao = new PetShopDAO();
            ptDao.updateQtdFuncs(PetShop.getQtdFuncionarios());
            JOptionPane.showMessageDialog(rootPane, "Auxiliar cadastrado com sucesso!");
            mudarTela();
        }
    }

    private void mudarTela() {
        Menu m = new Menu();    
        m.setVisible(true);
        this.dispose();
    }

    private void listarCrmv() {
        VeterinarioDAO vetDao = new VeterinarioDAO();
        ArrayList<Veterinario> listaVets = vetDao.buscaVeterinarios();
        
        String crmvs = null;
        
        for (int i = 0; i < listaVets.size(); i++) {
            crmvs = (String) listaVets.get(i).getCRMV();
            crmvBox.addItem(crmvs);
        }
    }
    
